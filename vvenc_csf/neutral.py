from __future__ import annotations

import csv
import re
from dataclasses import dataclass
from pathlib import Path

from vvenc_csf.core import CommandRunner, file_md5, files_equal, ffprobe_size, repo_path, write_csv
from vvenc_csf.encoding import EncodeJob, EncoderRunner, ImageConverter


COMMON_DEF_URL = (
    "https://github.com/fraunhoferhhi/vvenc/blob/"
    "6f767486ba2199d9f3f8a75b7739995bd3ed9034/"
    "source/Lib/CommonLib/CommonDef.h#L381"
)


@dataclass(frozen=True)
class Neutral16ControlConfig:
    root: Path
    png_dir: Path
    baseline_encoder: Path
    csf_encoder: Path
    qps: list[int]
    preset: str
    csv_path: Path
    report_path: Path


class Neutral16ControlRunner:
    def __init__(self, config: Neutral16ControlConfig, runner: CommandRunner | None = None) -> None:
        self.config = config
        self.runner = runner or CommandRunner()
        self.converter = ImageConverter(self.runner)
        self.encoder = EncoderRunner(self.runner)

    def run(self) -> list[dict[str, object]]:
        rows = self.collect_rows()
        write_csv(self.config.csv_path, rows)
        Neutral16ControlReport().write(self.config.report_path, self.config.csv_path, rows)
        return rows

    def collect_rows(self) -> list[dict[str, object]]:
        images = sorted(self.config.png_dir.glob("*.png"))
        if not images:
            raise RuntimeError(f"No PNG files found in {self.config.png_dir}")

        rows: list[dict[str, object]] = []
        for image in images:
            width, height = ffprobe_size(image, self.runner)
            name = image.stem
            yuv = self.config.root / "yuv" / f"{name}_{width}x{height}_1.yuv"
            self.converter.to_yuv420p(image, yuv)

            for qp in self.config.qps:
                rows.append(self._run_pair(name, yuv, width, height, qp))
        return rows

    def _run_pair(self, name: str, yuv: Path, width: int, height: int, qp: int) -> dict[str, object]:
        enc_dir = self.config.root / "encoded"
        base_dir = enc_dir / name / f"QP{qp}" / "baseline"
        csf_dir = enc_dir / name / f"QP{qp}" / "csf_off"
        baseline_bitstream = base_dir / f"{name}_QP{qp}_baseline.vvc"
        baseline_recon = base_dir / f"{name}_QP{qp}_baseline_rec.yuv"
        csf_bitstream = csf_dir / f"{name}_QP{qp}_csf_off.vvc"
        csf_recon = csf_dir / f"{name}_QP{qp}_csf_off_rec.yuv"

        self.encoder.encode(
            EncodeJob(
                encoder=self.config.baseline_encoder,
                yuv=yuv,
                width=width,
                height=height,
                qp=qp,
                preset=self.config.preset,
                bitstream=baseline_bitstream,
                recon=baseline_recon,
                log=base_dir / f"{name}_QP{qp}_baseline.log",
            )
        )
        self.encoder.encode(
            EncodeJob(
                encoder=self.config.csf_encoder,
                yuv=yuv,
                width=width,
                height=height,
                qp=qp,
                preset=self.config.preset,
                bitstream=csf_bitstream,
                recon=csf_recon,
                log=csf_dir / f"{name}_QP{qp}_csf_off.log",
                extra_args=("--CSFScalingList", "0"),
            )
        )

        return {
            "image": name,
            "width": width,
            "height": height,
            "qp": qp,
            "baseline_bitstream_bytes": baseline_bitstream.stat().st_size,
            "csf_off_bitstream_bytes": csf_bitstream.stat().st_size,
            "bitstream_identical": files_equal(baseline_bitstream, csf_bitstream),
            "reconstruction_identical": files_equal(baseline_recon, csf_recon),
            "baseline_bitstream_md5": file_md5(baseline_bitstream),
            "csf_off_bitstream_md5": file_md5(csf_bitstream),
            "baseline_recon_md5": file_md5(baseline_recon),
            "csf_off_recon_md5": file_md5(csf_recon),
        }


class Neutral16ControlReport:
    def write(self, markdown_path: Path, csv_path: Path, rows: list[dict[str, object]]) -> None:
        total = len(rows)
        bitstream_matches = sum(1 for row in rows if row["bitstream_identical"])
        reconstruction_matches = sum(1 for row in rows if row["reconstruction_identical"])
        all_passed = bitstream_matches == total and reconstruction_matches == total

        lines = [
            "# Neutral 16 Control Run",
            "",
            "This control checks the practical baseline for the neutral scaling-list value `16`.",
            "",
            "The control is generated by [vvenc_csf/neutral.py](../../vvenc_csf/neutral.py). `Neutral16ControlRunner` runs the default encoder and the CSF encoder with `--CSFScalingList 0`; `Neutral16ControlReport` writes this Markdown report from the collected rows.",
            "",
            "Current VVenC application binaries do not expose a `--ScalingListFile` option. Because of that, the external-file test used by VTM-style workflows is not used here. The control is split into two checks:",
            "",
            f"1. [neutral_16_verification.md](neutral_16_verification.md) verifies from upstream VVenC source code that [`LOG2_SCALING_LIST_NEUTRAL_VALUE = 4`]({COMMON_DEF_URL}), so the neutral scaling-list entry is `16`.",
            "2. This run verifies that the modified CSF encoder with `--CSFScalingList 0` produces the same bitstream and reconstruction as the default encoder under identical image/QP conditions.",
            "",
            f"CSV: [`{repo_path(csv_path)}`](neutral_16_control.csv)",
            "",
            "## Summary",
            "",
            "| Check | Passed | Total |",
            "| --- | ---: | ---: |",
            f"| Bitstream identity | {bitstream_matches} | {total} |",
            f"| Reconstruction identity | {reconstruction_matches} | {total} |",
            "",
            f"Overall result: `{'PASS' if all_passed else 'FAIL'}`.",
            "",
            "## Per-Image/QP Results",
            "",
            "| Image | QP | Bitstream bytes | Bitstream identical | Reconstruction identical |",
            "| --- | ---: | ---: | --- | --- |",
        ]
        for row in rows:
            lines.append(
                f"| {row['image']} | {row['qp']} | {row['baseline_bitstream_bytes']} | "
                f"{row['bitstream_identical']} | {row['reconstruction_identical']} |"
            )
        lines.append("")

        markdown_path.parent.mkdir(parents=True, exist_ok=True)
        markdown_path.write_text("\n".join(lines), encoding="utf-8", newline="\n")


class NeutralScalingVerifier:
    def __init__(self, vvenc_root: Path) -> None:
        self.vvenc_root = vvenc_root

    def render_report(self) -> str:
        common_def = self.vvenc_root / "source/Lib/CommonLib/CommonDef.h"
        common_def_text = common_def.read_text(encoding="utf-8", errors="replace")

        log2_neutral = self._parse_constant(common_def_text, "LOG2_SCALING_LIST_NEUTRAL_VALUE")
        scaling_list_bits = self._parse_constant(common_def_text, "SCALING_LIST_BITS")
        neutral_value = 1 << log2_neutral

        default_stats = [self._matrix_stats(path, neutral_value) for path in sorted((Path(__file__).resolve().parent.parent / "docs/matrices").glob("default_*.csv"))]
        all_default_neutral = all(bool(row["all_neutral"]) for row in default_stats)

        lines = [
            "# Neutral Scaling-List Value Verification",
            "",
            "This report verifies the neutral scaling-list value used by the upstream VVenC code and by the default matrix snapshots in this repository.",
            "",
            "The verification is generated by [vvenc_csf/neutral.py](../../vvenc_csf/neutral.py). `NeutralScalingVerifier.render_report()` reads `source/Lib/CommonLib/CommonDef.h`, `_parse_constant()` extracts the constants, and `_matrix_stats()` checks the stored default matrix CSV files.",
            "",
            "## Source Constants",
            "",
            "| Constant | Value | Meaning |",
            "| --- | ---: | --- |",
            f"| `SCALING_LIST_BITS` | {scaling_list_bits} | Bit depth of scaling-list entries. |",
            f"| [`LOG2_SCALING_LIST_NEUTRAL_VALUE`]({COMMON_DEF_URL}) | {log2_neutral} | Log2 of the value that has no effect on quantisation. |",
            f"| Neutral value | {neutral_value} | Computed as `1 << LOG2_SCALING_LIST_NEUTRAL_VALUE`. |",
            "",
            "## Default Matrix Snapshots",
            "",
            "| File | Entries | Min | Max | All entries are neutral |",
            "| --- | ---: | ---: | ---: | --- |",
        ]
        for row in default_stats:
            lines.append(f"| `{row['file']}` | {row['entries']} | {row['min']} | {row['max']} | {row['all_neutral']} |")

        lines.extend(
            [
                "",
                "## Quant/Dequant Equivalence",
                "",
                f"In VVC scaling-list syntax, [`LOG2_SCALING_LIST_NEUTRAL_VALUE = 4`]({COMMON_DEF_URL}) defines `16` as the no-op scaling-list entry.",
                "",
                "For a scaling-list weight `w`, the neutral case is:",
                "",
                "```text",
                "w = 1 << LOG2_SCALING_LIST_NEUTRAL_VALUE = 16",
                "```",
                "",
                "The practical CSF-off control run verifies that the modified encoder preserves default behavior when CSF scaling lists are disabled.",
                "",
                "## Result",
                "",
                f"- Default matrix snapshots are all neutral: `{all_default_neutral}`.",
                f"- Neutral scaling-list value is `16`: `{neutral_value == 16}`.",
                "",
            ]
        )
        return "\n".join(lines)

    def write_report(self, output: Path) -> None:
        output.parent.mkdir(parents=True, exist_ok=True)
        output.write_text(self.render_report(), encoding="utf-8", newline="\n")

    @staticmethod
    def _parse_constant(text: str, name: str) -> int:
        match = re.search(rf"\b{name}\s*=\s*(\d+)", text)
        if not match:
            raise RuntimeError(f"Could not find {name}")
        return int(match.group(1))

    @staticmethod
    def _matrix_stats(path: Path, neutral_value: int) -> dict[str, object]:
        values: list[int] = []
        with path.open("r", encoding="utf-8-sig", newline="") as stream:
            for row in csv.reader(stream):
                values.extend(int(value) for value in row if value != "")

        if not values:
            raise RuntimeError(f"No matrix values found in {path}")

        return {
            "file": repo_path(path),
            "entries": len(values),
            "min": min(values),
            "max": max(values),
            "all_neutral": all(value == neutral_value for value in values),
        }

