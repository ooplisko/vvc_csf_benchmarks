from __future__ import annotations

import argparse
import csv
import re
from pathlib import Path


ROOT = Path(__file__).resolve().parent.parent
COMMON_DEF_URL = (
    "https://github.com/fraunhoferhhi/vvenc/blob/"
    "6f767486ba2199d9f3f8a75b7739995bd3ed9034/"
    "source/Lib/CommonLib/CommonDef.h#L381"
)


def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8", errors="replace")


def parse_constant(text: str, name: str) -> int:
    match = re.search(rf"\b{name}\s*=\s*(\d+)", text)
    if not match:
        raise RuntimeError(f"Could not find {name}")
    return int(match.group(1))


def matrix_stats(path: Path, neutral_value: int) -> dict[str, object]:
    values: list[int] = []
    with path.open("r", encoding="utf-8-sig", newline="") as stream:
        for row in csv.reader(stream):
            values.extend(int(value) for value in row if value != "")

    if not values:
        raise RuntimeError(f"No matrix values found in {path}")

    return {
        "file": path.relative_to(ROOT).as_posix(),
        "entries": len(values),
        "min": min(values),
        "max": max(values),
        "all_neutral": all(value == neutral_value for value in values),
    }


def render_report(args: argparse.Namespace) -> str:
    common_def = args.vvenc_root / "source/Lib/CommonLib/CommonDef.h"
    common_def_text = read_text(common_def)

    log2_neutral = parse_constant(common_def_text, "LOG2_SCALING_LIST_NEUTRAL_VALUE")
    scaling_list_bits = parse_constant(common_def_text, "SCALING_LIST_BITS")
    neutral_value = 1 << log2_neutral

    default_stats = [
        matrix_stats(path, neutral_value)
        for path in sorted((ROOT / "docs/matrices").glob("default_*.csv"))
    ]

    all_default_neutral = all(bool(row["all_neutral"]) for row in default_stats)

    lines = [
        "# Neutral Scaling-List Value Verification",
        "",
        "This report verifies the neutral scaling-list value used by the upstream VVenC code and by the default matrix snapshots in this repository.",
        "",
        "The verification is generated by [tools/verify_neutral_scaling.py](../../tools/verify_neutral_scaling.py). Its `render_report()` function reads `source/Lib/CommonLib/CommonDef.h`, `parse_constant()` extracts the constants, and `matrix_stats()` checks the stored default matrix CSV files.",
        "",
        "## Source Constants",
        "",
        "| Constant | Value | Meaning |",
        "| --- | ---: | --- |",
        f"| `SCALING_LIST_BITS` | {scaling_list_bits} | Bit depth of scaling-list entries. |",
        f"| [`LOG2_SCALING_LIST_NEUTRAL_VALUE`]({COMMON_DEF_URL}) | {log2_neutral} | Log2 of the value that has no effect on quantisation. |",
        f"| Neutral value | {neutral_value} | Computed as `1 << LOG2_SCALING_LIST_NEUTRAL_VALUE`. |",
        "",
        "## Default Matrix Snapshots",
        "",
        "| File | Entries | Min | Max | All entries are neutral |",
        "| --- | ---: | ---: | ---: | --- |",
    ]
    for row in default_stats:
        lines.append(
            f"| `{row['file']}` | {row['entries']} | {row['min']} | {row['max']} | {row['all_neutral']} |"
        )

    lines.extend(
        [
            "",
            "## Quant/Dequant Equivalence",
            "",
            f"In VVC scaling-list syntax, [`LOG2_SCALING_LIST_NEUTRAL_VALUE = 4`]({COMMON_DEF_URL}) defines `16` as the no-op scaling-list entry.",
            "",
            "For a scaling-list weight `w`, the neutral case is:",
            "",
            "```text",
            "w = 1 << LOG2_SCALING_LIST_NEUTRAL_VALUE = 16",
            "```",
            "",
            "The practical CSF-off control run verifies that the modified encoder preserves default behavior when CSF scaling lists are disabled.",
            "",
            "## Result",
            "",
            f"- Default matrix snapshots are all neutral: `{all_default_neutral}`.",
            f"- Neutral scaling-list value is `16`: `{neutral_value == 16}`.",
            "",
        ]
    )
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description="Verify that scaling-list value 16 is neutral in VVenC.")
    parser.add_argument("--vvenc-root", type=Path, default=ROOT.parent / "vvenc")
    parser.add_argument("--output", type=Path, default=ROOT / "docs/matrices/neutral_16_verification.md")
    args = parser.parse_args()

    report = render_report(args)
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(report, encoding="utf-8", newline="\n")
    print(f"Wrote {args.output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
