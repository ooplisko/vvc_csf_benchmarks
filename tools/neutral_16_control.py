from __future__ import annotations

import argparse
import csv
import hashlib
import subprocess
from pathlib import Path


ROOT = Path(__file__).resolve().parent.parent
COMMON_DEF_URL = (
    "https://github.com/fraunhoferhhi/vvenc/blob/"
    "6f767486ba2199d9f3f8a75b7739995bd3ed9034/"
    "source/Lib/CommonLib/CommonDef.h#L381"
)


def run(cmd: list[str], log_file: Path | None = None) -> str:
    result = subprocess.run(cmd, text=True, stdout=subprocess.PIPE, stderr=subprocess.STDOUT)
    if log_file is not None:
        log_file.parent.mkdir(parents=True, exist_ok=True)
        log_file.write_text(result.stdout, encoding="utf-8", errors="replace")
    if result.returncode != 0:
        raise RuntimeError(f"Command failed ({result.returncode}): {' '.join(cmd)}\n{result.stdout}")
    return result.stdout


def ffprobe_size(path: Path) -> tuple[int, int]:
    out = run(
        [
            "ffprobe",
            "-v",
            "error",
            "-select_streams",
            "v:0",
            "-show_entries",
            "stream=width,height",
            "-of",
            "csv=s=x:p=0",
            str(path),
        ]
    )
    width, height = out.strip().split("x")
    return int(width), int(height)


def file_md5(path: Path) -> str:
    digest = hashlib.md5()
    with path.open("rb") as stream:
        while chunk := stream.read(1024 * 1024):
            digest.update(chunk)
    return digest.hexdigest()


def files_equal(left: Path, right: Path) -> bool:
    if left.stat().st_size != right.stat().st_size:
        return False
    with left.open("rb") as left_stream, right.open("rb") as right_stream:
        while True:
            left_chunk = left_stream.read(1024 * 1024)
            right_chunk = right_stream.read(1024 * 1024)
            if left_chunk != right_chunk:
                return False
            if not left_chunk:
                return True


def repo_path(path: Path) -> str:
    resolved = path.resolve()
    try:
        return resolved.relative_to(ROOT).as_posix()
    except ValueError:
        return resolved.as_posix()


def encode(
    encoder: Path,
    yuv: Path,
    width: int,
    height: int,
    qp: int,
    preset: str,
    bitstream: Path,
    recon: Path,
    log: Path,
    extra_args: list[str],
) -> None:
    bitstream.parent.mkdir(parents=True, exist_ok=True)
    cmd = [
        str(encoder),
        "--InputFile",
        str(yuv),
        "--SourceWidth",
        str(width),
        "--SourceHeight",
        str(height),
        "--FrameRate",
        "1",
        "--FramesToBeEncoded",
        "1",
        "--QP",
        str(qp),
        "--preset",
        preset,
        "--BitstreamFile",
        str(bitstream),
        "--ReconFile",
        str(recon),
    ]
    cmd.extend(extra_args)
    run(cmd, log)


def write_report(markdown_path: Path, csv_path: Path, rows: list[dict[str, object]]) -> None:
    total = len(rows)
    bitstream_matches = sum(1 for row in rows if row["bitstream_identical"])
    reconstruction_matches = sum(1 for row in rows if row["reconstruction_identical"])
    all_passed = bitstream_matches == total and reconstruction_matches == total

    lines = [
        "# Neutral 16 Control Run",
        "",
        "This control checks the practical baseline for the neutral scaling-list value `16`.",
        "",
        "The control is generated by [tools/neutral_16_control.py](../../tools/neutral_16_control.py). Its `encode()` function runs the default encoder and the CSF encoder with `--CSFScalingList 0`; `files_equal()` then checks bitstream and reconstruction identity.",
        "",
        "Current VVenC application binaries do not expose a `--ScalingListFile` option. Because of that, the external-file test used by VTM-style workflows is not used here. The control is split into two checks:",
        "",
        f"1. [neutral_16_verification.md](neutral_16_verification.md) verifies from upstream VVenC source code that [`LOG2_SCALING_LIST_NEUTRAL_VALUE = 4`]({COMMON_DEF_URL}), so the neutral scaling-list entry is `16`.",
        "2. This run verifies that the modified CSF encoder with `--CSFScalingList 0` produces the same bitstream and reconstruction as the default encoder under identical image/QP conditions.",
        "",
        f"CSV: [`{repo_path(csv_path)}`](neutral_16_control.csv)",
        "",
        "## Summary",
        "",
        "| Check | Passed | Total |",
        "| --- | ---: | ---: |",
        f"| Bitstream identity | {bitstream_matches} | {total} |",
        f"| Reconstruction identity | {reconstruction_matches} | {total} |",
        "",
        f"Overall result: `{'PASS' if all_passed else 'FAIL'}`.",
        "",
        "## Per-Image/QP Results",
        "",
        "| Image | QP | Bitstream bytes | Bitstream identical | Reconstruction identical |",
        "| --- | ---: | ---: | --- | --- |",
    ]
    for row in rows:
        lines.append(
            f"| {row['image']} | {row['qp']} | {row['baseline_bitstream_bytes']} | "
            f"{row['bitstream_identical']} | {row['reconstruction_identical']} |"
        )
    lines.append("")

    markdown_path.parent.mkdir(parents=True, exist_ok=True)
    markdown_path.write_text("\n".join(lines), encoding="utf-8", newline="\n")


def main() -> int:
    parser = argparse.ArgumentParser(description="Run a neutral-16 control for default VVenC vs CSF-off VVenC.")
    parser.add_argument("--root", type=Path, default=Path("results/neutral_16_control"), help="Temporary run directory.")
    parser.add_argument("--png-dir", type=Path, default=Path("image_sets/synthetic/png"), help="Input PNG directory.")
    parser.add_argument("--baseline-encoder", type=Path, default=Path("binaries/vvenc_default.exe"))
    parser.add_argument("--csf-encoder", type=Path, default=Path("binaries/vvenc_csf.exe"))
    parser.add_argument("--qps", default="22,27,32,37", help="Comma-separated QP list.")
    parser.add_argument("--preset", default="medium")
    parser.add_argument("--csv", type=Path, default=Path("docs/matrices/neutral_16_control.csv"))
    parser.add_argument("--report", type=Path, default=Path("docs/matrices/neutral_16_control.md"))
    args = parser.parse_args()

    qps = [int(item) for item in args.qps.split(",") if item.strip()]
    images = sorted(args.png_dir.glob("*.png"))
    if not images:
        raise RuntimeError(f"No PNG files found in {args.png_dir}")

    yuv_dir = args.root / "yuv"
    enc_dir = args.root / "encoded"
    rows: list[dict[str, object]] = []

    for image in images:
        width, height = ffprobe_size(image)
        name = image.stem
        yuv = yuv_dir / f"{name}_{width}x{height}_1.yuv"
        if not yuv.exists():
            yuv.parent.mkdir(parents=True, exist_ok=True)
            run(["ffmpeg", "-y", "-v", "error", "-i", str(image), "-pix_fmt", "yuv420p", "-frames:v", "1", str(yuv)])

        for qp in qps:
            base_dir = enc_dir / name / f"QP{qp}" / "baseline"
            csf_dir = enc_dir / name / f"QP{qp}" / "csf_off"
            baseline_bitstream = base_dir / f"{name}_QP{qp}_baseline.vvc"
            baseline_recon = base_dir / f"{name}_QP{qp}_baseline_rec.yuv"
            csf_bitstream = csf_dir / f"{name}_QP{qp}_csf_off.vvc"
            csf_recon = csf_dir / f"{name}_QP{qp}_csf_off_rec.yuv"

            encode(
                args.baseline_encoder,
                yuv,
                width,
                height,
                qp,
                args.preset,
                baseline_bitstream,
                baseline_recon,
                base_dir / f"{name}_QP{qp}_baseline.log",
                [],
            )
            encode(
                args.csf_encoder,
                yuv,
                width,
                height,
                qp,
                args.preset,
                csf_bitstream,
                csf_recon,
                csf_dir / f"{name}_QP{qp}_csf_off.log",
                ["--CSFScalingList", "0"],
            )

            rows.append(
                {
                    "image": name,
                    "width": width,
                    "height": height,
                    "qp": qp,
                    "baseline_bitstream_bytes": baseline_bitstream.stat().st_size,
                    "csf_off_bitstream_bytes": csf_bitstream.stat().st_size,
                    "bitstream_identical": files_equal(baseline_bitstream, csf_bitstream),
                    "reconstruction_identical": files_equal(baseline_recon, csf_recon),
                    "baseline_bitstream_md5": file_md5(baseline_bitstream),
                    "csf_off_bitstream_md5": file_md5(csf_bitstream),
                    "baseline_recon_md5": file_md5(baseline_recon),
                    "csf_off_recon_md5": file_md5(csf_recon),
                }
            )

    args.csv.parent.mkdir(parents=True, exist_ok=True)
    with args.csv.open("w", encoding="utf-8", newline="") as stream:
        writer = csv.DictWriter(stream, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)

    write_report(args.report, args.csv, rows)
    print(f"Wrote {args.csv}")
    print(f"Wrote {args.report}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
